let blogCurrentSlide = 0;
let goingNextSlide = true;
let scrollLimit = blogList.length / 2 - 1;

const nextBlogSlide = function () {
  goingNextSlide = true;
  const sliderWidth = parseFloat(
    getComputedStyle(document.querySelector(".blog-slider")).width
  );
  if (scrollLimit == blogCurrentSlide) return;

  blogSliderArrowRight.style.opacity = 0.6;
  blogSliderArrowLeft.style.opacity = 0.6;
  blogCurrentSlide++;
  // blogSlides[blogCurrentSlide].scrollIntoView();
  blogsListELe.scrollLeft += sliderWidth;
  if (blogCurrentSlide === scrollLimit)
    blogSliderArrowRight.style.opacity = 0.3;
};
const prevBlogSlide = function () {
  goingNextSlide = true;
  const sliderWidth = parseFloat(
    getComputedStyle(document.querySelector(".blog-slider")).width
  );
  if (blogCurrentSlide === 0) {
    return;
  }
  blogSliderArrowLeft.style.opacity = 0.6;
  blogSliderArrowRight.style.opacity = 0.6;
  blogCurrentSlide--;
  // blogSlides[blogCurrentSlide].scrollIntoView();
  blogsListELe.scrollLeft -= sliderWidth;
  if (blogCurrentSlide === 0) {
    blogSliderArrowLeft.style.opacity = 0.3;
  }
};

blogSliderArrowRight.addEventListener("click", nextBlogSlide);
blogSliderArrowLeft.addEventListener("click", prevBlogSlide);
